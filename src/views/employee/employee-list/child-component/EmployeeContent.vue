          this.$store.commit("addToast", {
            type: "error",
            text: this.$t("errorHandle.employeeSubsystem.notFoundEmployee"),
          });

          this.reloadData();
        }
      } catch (error) {
        console.log(
          "üöÄ ~ file: EmployeeContent.vue:351 ~ openConfirmDeletePopup ~ error:",
          error
        );
      }
    },

    /**
     * m·ªü form nh√¢n b·∫£n
