      this.reloadData();
    },

    /**
     * Th·ª±c hi·ªán l·∫•y l·∫°i m·∫´u m·∫∑c ƒë·ªãnh c·ªßa layout
     * @author: TTANH (04/08/2023)
     */
    async SetDefaultLayout() {
      this.isShowLayoutSetting = false;

      const res = await EmployeeLayoutService.setDefaultLayout();

      if (res) {
      } else {
