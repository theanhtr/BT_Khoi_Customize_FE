        const res = await EmployeeService.getExcel({
          searchText: this.searchText,
        });

        this.isLoading = false;

        if (res.success) {
          this.$store.commit("addToast", {
            type: "success",
            text: this.$t("successHandle.employeeSubsystem.exportExcel"),
          });
        } else {
          CommonErrorHandle();
        }
      } catch (error) {
        console.log(
          "üöÄ ~ file: EmployeeContent.vue:346 ~ exportToExcelWithSearchText ~ error:",
          error
        );
      }
    },

    /**
     * h√†m x·ª≠ l√Ω vi·ªác ·∫•n v√†o item c·ªßa dropdown n√∫t "Th√™m"
     * @author: TTANH (19/07/2023)
