    openConfirmDeletePopup(id) {
      try {
        let index = findIndexByAttribute(this.employees, "EmployeeId", id);

        if (index !== -1) {
          this.employeeCodeDelete = this.employees[index].EmployeeCode;
          this.employeeIdDelete = id;
          this.isShowConfirmDeletePopup = true;
